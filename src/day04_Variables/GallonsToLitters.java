package day04_Variables;

public class GallonsToLitters {
    public static void main(String[] args) {
         int gallon = 35;
         double litter = gallon * 3.79; // 1 gallon = 3.79 litters

        System.out.println("gallon = " + gallon);
        System.out.println("litter = " + litter);
    }
}
