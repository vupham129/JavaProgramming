package day04_Variables;

public class KgToPounds {
    public static void main(String[] args) {
        int kg = 2000;
        double pounds = kg * 2.2; // 1kg = 2.2 pounds

        System.out.println("kg = " + kg);
        System.out.println("pounds = " + pounds);
    }
}
