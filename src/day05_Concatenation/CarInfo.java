package day05_Concatenation;

public class CarInfo {
    public static void main(String[] args) {
        short year = 2017;
        String make = "Mercedes";
        String model = "GLC";
        int miles = 54000;
        String color = "Grey";
        int price = 57000;

        System.out.println(year + " " + make + " " + model + ", " + miles + " miles, " + color + ", $" + price + ".");
    }
}
