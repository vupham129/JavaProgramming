package day08_IfStatements;

public class SwapTwoVariables_WithoutTemporaryVariable {
    public static void main(String[] args) {
        int a = 10;
        int b = 15;

        a = a + 5;
        b = b - 5;

        System.out.println("a = " + a);
        System.out.println("b = " + b);

    }
}
