package Practice_12_15_2021;

public class SwapTwoNumbers {
    public static void main(String[] args) {
        int a = 10;
        int b = 15;

        // a= 15, b = 10
        int temp = a;
        a=b;
        b=temp;

        System.out.println("a = " + a);
        System.out.println("b = " + b);


    }
}
